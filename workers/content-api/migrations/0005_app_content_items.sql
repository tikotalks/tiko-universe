-- Generic app content model for app defaults and future user-owned content.
-- Existing content_items rows from the CMS model remain valid; the app-specific
-- columns are nullable so older query methods can keep working.

ALTER TABLE content_items ADD COLUMN app_id TEXT;
ALTER TABLE content_items ADD COLUMN type TEXT;
ALTER TABLE content_items ADD COLUMN parent_id TEXT;
ALTER TABLE content_items ADD COLUMN source_locale TEXT NOT NULL DEFAULT 'en';
ALTER TABLE content_items ADD COLUMN subtitle TEXT;
ALTER TABLE content_items ADD COLUMN body TEXT;
ALTER TABLE content_items ADD COLUMN speech TEXT;
ALTER TABLE content_items ADD COLUMN color_token TEXT;
ALTER TABLE content_items ADD COLUMN color_hex INTEGER;
ALTER TABLE content_items ADD COLUMN icon TEXT;
ALTER TABLE content_items ADD COLUMN image_ref TEXT;
ALTER TABLE content_items ADD COLUMN image_url TEXT;
ALTER TABLE content_items ADD COLUMN sort_order INTEGER NOT NULL DEFAULT 0;
ALTER TABLE content_items ADD COLUMN is_default INTEGER NOT NULL DEFAULT 0;
ALTER TABLE content_items ADD COLUMN is_published INTEGER NOT NULL DEFAULT 1;
ALTER TABLE content_items ADD COLUMN owner_user_id TEXT;
ALTER TABLE content_items ADD COLUMN owner_child_id TEXT;
ALTER TABLE content_items ADD COLUMN source_item_id TEXT;
ALTER TABLE content_items ADD COLUMN metadata_json TEXT;

CREATE TABLE IF NOT EXISTS content_item_translations (
  item_id TEXT NOT NULL,
  locale TEXT NOT NULL,
  title TEXT,
  subtitle TEXT,
  body TEXT,
  speech TEXT,
  metadata_json TEXT,
  created_at TEXT NOT NULL DEFAULT (datetime('now')),
  updated_at TEXT NOT NULL DEFAULT (datetime('now')),
  PRIMARY KEY (item_id, locale),
  FOREIGN KEY (item_id) REFERENCES content_items(id) ON DELETE CASCADE
);

CREATE INDEX IF NOT EXISTS idx_content_items_app ON content_items(app_id, type, parent_id, sort_order);
CREATE INDEX IF NOT EXISTS idx_content_items_owner ON content_items(owner_user_id, owner_child_id, app_id);
CREATE INDEX IF NOT EXISTS idx_content_items_source ON content_items(source_item_id);
CREATE INDEX IF NOT EXISTS idx_content_item_translations_locale ON content_item_translations(locale);

