const payload = { locale: 'en', content, translateMissing: true }
const importUrl = `${LEZU_BASE_URL}/v1/i18n/projects/${PROJECT_ID}/import`

if (isDryRun) {
  console.log('Dry run - would POST to:', importUrl)
  console.log(`Keys: ${Object.keys(content).length}`)
  console.log(JSON.stringify(payload, null, 2))
  process.exit(0)
}

console.log(`Importing ${Object.keys(content).length} keys directly to Lezu...`)

const res = await fetch(importUrl, {
  method: 'POST',
  headers: {
    'Authorization': `${AUTH_SCHEME} ${API_KEY}`,
    'Content-Type': 'application/json',
  },
  body: JSON.stringify(payload),
})

const body = await res.json()
if (res.ok) {
  console.log('Success:', JSON.stringify(body, null, 2))
} else {
  console.error(`Error (${res.status}):`, JSON.stringify(body, null, 2))
  process.exit(1)
}
