import { readFile, writeFile } from 'node:fs/promises'
import { dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const root = resolve(dirname(fileURLToPath(import.meta.url)), '..')
const sourcePath = resolve(root, 'packages/ui/tiko-colors.json')
const webPath = resolve(root, 'packages/ui/src/tikoColors.ts')
const dataPath = resolve(root, 'packages/data/src/tikoColors.ts')
const iosPath = resolve(root, 'packages/tikokit-ios/Sources/TikoKit/TikoColors.swift')

const colors = JSON.parse(await readFile(sourcePath, 'utf8'))

for (const color of colors) {
  if (!/^[a-z][a-z0-9-]*$/.test(color.name)) {
    throw new Error(`Invalid color name: ${color.name}`)
  }
  if (!/^#[0-9A-F]{6}$/i.test(color.hex)) {
    throw new Error(`Invalid hex for ${color.name}: ${color.hex}`)
  }
}

const web = `// Generated by tools/generate-tiko-colors.mjs from packages/ui/tiko-colors.json.
// Do not edit this file directly.

export interface TikoColorEntry {
  name: string
  hex: string
}

export const tikoColors = ${JSON.stringify(colors, null, 2)} as const

export type TikoColorName = typeof tikoColors[number]['name']
`

const data = `// Generated by tools/generate-tiko-colors.mjs from packages/ui/tiko-colors.json.
// Do not edit this file directly.

export type TikoColorName = ${colors.map((color) => `'${color.name}'`).join(' | ')}
`

const swiftStatics = colors
  .map((color) => {
    const hex = `0x${color.hex.slice(1).toUpperCase()}`
    return `    public static let ${color.name.padEnd(7)} = TikoColor(name: "${color.name}",${' '.repeat(Math.max(1, 8 - color.name.length))}hex: ${hex})`
  })
  .join('\n')

const swiftAll = colors
  .map((color) => color.name)
  .reduce((lines, name, index) => {
    const lineIndex = Math.floor(index / 5)
    lines[lineIndex] = [...(lines[lineIndex] ?? []), name]
    return lines
  }, [])
  .map((line) => `        ${line.join(', ')},`)
  .join('\n')

const swift = `// Generated by tools/generate-tiko-colors.mjs from packages/ui/tiko-colors.json.
// Do not edit this file directly.

import SwiftUI

public struct TikoColor: Sendable {
    public let name: String
    public let hex: UInt32

    public var color: Color { Color(hex: hex) }
}

public enum TikoColors {
${swiftStatics}

    public static let all: [TikoColor] = [
${swiftAll}
    ]

    public static let allNames: [String] = all.map(\\.name)

    public static func named(_ name: String) -> TikoColor? {
        all.first { $0.name == name }
    }

    public static func hex(named name: String, fallback: UInt32 = gray.hex) -> UInt32 {
        named(name)?.hex ?? fallback
    }

    public static func color(named name: String) -> Color? {
        named(name)?.color
    }
}
`

await writeFile(webPath, web)
await writeFile(dataPath, data)
await writeFile(iosPath, swift)
